/*
ASSIGNMENT OPERORATORS
*/

let r = 5 
// our = is the assignment 

// + assignment 
 r +=1; // r = r + 1 or, 5 + 1 in this case 
 console.log(r);

 // subtraction assignment 
 r -= 2; //  r = r -2 
 console.log(r);

 // mulitplicaton assignment 
 r *= 2; // same as r = r *2
 console.log(r);

 // division assignment 
 r /= 1;
 console.log(r);

 // exponential assignmnet 
 r **= 3; // r = r 
 console.log(r);

 // remainder (modulus) assignment 
 r %= 1; //same as r = r % 1
 console.log(r);

 let y = 5; 
 console.log(y);
 y %= 2; 
 console.log(y);